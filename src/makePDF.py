def makePdf(fileName):
    pdf = canvas.Canvas(fileName + ".pdf", pagesize=portrait(A4))
    pdfmetrics.registerFont(UnicodeCIDFont('HeiseiKakuGo-W5'))
    generateHeader(pdf)
    generateLine(pdf)
    pdf.save()
